/**
 * Get file type icon component by file extension
 * @param extension File extension (e.g., "pdf", "docx", "jpg")
 * @returns React component for the file type icon
 * 
 * @example
 * <GetFileIcon extension="pdf" />  // Returns PDF icon
 * <GetFileIcon extension="js" />   // Returns JS icon
 * <GetFileIcon extension="unknown" /> // Returns OTHER icon as fallback
 */
export function GetFileIcon({ extension }: { extension: string }): JSX.Element {
  const ext = extension.toLowerCase().replace(/^\./, ''); // Remove leading dot if present

  const iconMap: Record<string, () => JSX.Element> = {
    css: CSS,
    csv: CSV,
    doc: DOC,
    docx: DOCX,
    exe: EXE,
    fig: FIG,
    html: HTML,
    java: JAVA,
    js: JS,
    json: JSON,
    pdf: PDF,
    ppt: PPT,
    pptx: PPTX,
    rar: RAR,
    sql: SQL,
    txt: TXT,
    xls: XLS,
    xlsx: XLSX,
    xml: XML,
    zip: ZIP,
  };

  const IconComponent = iconMap[ext];
  return IconComponent ? <IconComponent /> : <OTHER />;
}